const main = async () => {
    const [deployer] = await hre.ethers.getSigners();
    const accountBalance = await deployer.getBalance();
  
    console.log("Deploying contracts with account: ", deployer.address);
    console.log("Account balance: ", accountBalance.toString());
  
    const coinFactory = await hre.ethers.getContractFactory("CoinFlipAttack");
    const coinContract = await coinFactory.deploy();
    await coinContract.deployed();
  
    console.log("CoinFlipAttack address: ", coinContract.address);
  };
  
  const runMain = async () => {
    try {
      await main();
      process.exit(0);
    } catch (error) {
      console.log(error);
      process.exit(1);
    }
  };
  
  runMain();